// lambda, or closure.
// An anonymous function
// behaves the same as a regular function,
//but it does not have a name with it. It can have zero or any number of arguments
/// parameters with an option type annotation.
void main() {
  const fruits = ["APPle", "Mango", "Avocando", "Orange"];
  fruits.forEach((fruits) {
    print(fruits);
  });
  const phones = ["Tecno", "Iphone", "Sumsang", "Itel", "Oppo"];
  phones.forEach((fruits) {
    print(fruits);
  });
}
